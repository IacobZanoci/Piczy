import CoreGraphics

public extension CGFloat {
    
    /// A size equivalent to `4.0` points.
    static let extraSmall: CGFloat = 4.0
    
    /// A size equivalent to `8.0` points.
    static let small: CGFloat = 8.0
    
    /// A size equivalent to `16.0` points.
    static let medium: CGFloat = 16.0
    
    /// A size equivalent to `24.0` points.
    static let large: CGFloat = 24.0
    
    /// A size equivalent to `32.0` points.
    static let extraLarge: CGFloat = 32.0
}
